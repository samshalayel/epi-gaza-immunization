function getLang() {
  return localStorage.getItem('lang') || 'ar';
}
function setLang(l) {
  localStorage.setItem('lang', l);
}
function t(key) {
  const l = getLang();
  return (TRANSLATIONS[l] && TRANSLATIONS[l][key]) || (TRANSLATIONS['en'][key]) || key;
}
function applyDir() {
  const l = getLang();
  document.documentElement.lang = TRANSLATIONS[l].lang;
  document.documentElement.dir  = TRANSLATIONS[l].dir;
}
